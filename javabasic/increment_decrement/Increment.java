package javabasic.increment_decrement;

public class Increment {
    public static void main(String args[]){
        int i = 0;
        //post increment
        System.out.println("after post increemnt: "+ i++);//her 1st value will be 0 after printing 1st value i.e 0 , it will increment. 

        
        //pre increment
        System.out.println("Pre increment: "+ ++i); //value is first incremented and then printed
    }
}
